#include<bits/stdc++.h>

using namespace std;
const int MAXN = 1e5 + 5;
int n, m, a[MAXN];
bool b[MAXN] = {0};
deque<int> c;
void Solve(){
  for (int i = 0; i <= n; i++){
    b[i] = 0;
  }
  cin >> n >> m;
  c.clear();
  for (int i = 1; i <= n; i++){
    cin >> a[i];
    c.push_back(a[i]);
    b[a[i]] = 1;
  }
  int mex = 0, p;
  for (int i = 0; i <= n; i++){
    if (!b[i]){
      mex = i;
      break;
    }
  }
  int eend = (m <= n ? m : m / n + 2);
  for (int i = 1; i <= eend; i++){
    for (auto v : c){
      cout << v << ' ';
    }
    cout << '\n';
    cout << mex << '\n';
    c.push_front(mex);
    auto p = c.end();
    p--;
    mex = *p;
    c.pop_back();

  }
  for (auto v : c){
    cout << v << ' ';
  }
  cout << '\n';
}
int main(){
  ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);
  int t;
  cin >> t;
  while (t--){
    Solve();
  }
  return 0;
}
